package com.bilgeadam.boost.week02.lesson005;

import java.util.Scanner;

public class Arrays {

	public static void main(String[] args) {
	
		
			
		 
		for (int i = 0; i <= 10; i=i+2) {
			int count=0;
			do {
			    System.out.print("*");
			    count++;
			} while (count<i);
			System.out.println();
			
		     
		}
		

	}

}
